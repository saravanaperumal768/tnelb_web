                <p class="text-dark font-weight-700"> 36. Handling of electric supply lines and apparatus: </p>
                <ul>
                    <li>(1) Before any conductor or apparatus is handled, adequate precautions shall be taken, by earthing or other suitable means, to discharge electrically such conductor or apparatus, and any adjacent conductor or apparatus if there is danger therefrom, and to prevent any conductor or apparatus from being acidentaly or inadvertently electrically charged when persons are working thereon. </li>
                    <li>[Every person who is working on an electric supply line or apparatus or both shall be provided with tools and devices,such as gloves, rubber shoes, safety belts, ladders, earthing devices, helmets, line testers, hand lines and the like for protecting him from mechanical and electrical injury. Such tools and devices shall always be maintained in sound and efficient working conditions]. </li>
                    <li>(2) No person shall work on any live electric supply lie or apparatus and no person shall assist such person on such work, Unless he is authorized in that behalf, and takes the safety measures approved by the Inspector. </li>
                    <li>(3) Every telecommunication line on supports carrying a high or extra-high voltage line shall, for the purpose of working thereon, be deemed to be a high voltage line.</li>
                </ul>

                <p class="text-dark font-weight-700"> 45. Precautions to be adopted by consumers, owners, [occupiers], electrical contractor, electrical workmen and suppliers: - </p>
                <ul>
                    <li>(1) No electrical installation work, including additions,alterations,repairs and adjustments to existing installations,execpt such replacement of lamps, fans, fuses, switches, low voltage domestic appliances and fittings as in no way alters its capacity or character, shall be carried out upon the premises of or on behalf of any [consumer, supplier, owner or occupier], for the purpose of supply to such consumer, owner, [or occupier] except by an electrical contractor licensed in this behalf by the State Government and under the direct supervision of a person holding a certificate of competency [ and by a person holding a permit ] issued or recognized by the State Government:</li>
                    <li>Provided that in the case of works executed for or on behalf of the central Government and in the case of installations in mines, oil-fields and railways, the Central Government and in other cases the State Government may, by notification in the Official Gazette, exempt, on such conditions as it may impose, any such work described therein either generally or in the case of any specified class of [consumer,Suppliers,Owners or Occupiers],from so much of this sub-rule as requires such work to be carried out by an electrical contractor licensed by the State Government in this behalf.</li>
                    <li>[(2) No electrical installation work which has been carried out in contravention of sub-rule (1) shall either be energized or connected to the works of any supplier.]</li>

                </ul>
