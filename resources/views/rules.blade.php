                    <li>3) The owner / occupier of a multi-storeyed building shall ensure that electrical installations / words inside the building are carried out and maintained in such a manner as to prevent danger due to shock and fire hazards, and the installation is carried out in accordance with the relevant codes of practices; </li>
                    <li>4) No other service pipes shall be taken along the ducts provided for laying power cables. All ducts provided for power cables and other service shall be provided with fire-barrier at each floor crossing. ] </li>

