                    <li>(2) Save as otherwise provided in these rules, the relevant code of practice of the [Bureau of Indian Standards], [including National Electrical Code], if any, may be followed to carry out the purposes of this rule and in the event of any inconsistency, the provisions of these rules shall prevail].</li>
